# 값을 여러 개 출력하기
print(1,2,3)
print("Hello", "Python")

# sep(구분자라는 뜻의 separator)로 값 사이에 문자 넣기
print(1, 2, 3, sep = ', ')
print('a', 'b', 'c', sep = ', ')
print('010', '1234', '5678', sep = '-')
print(1, 2, 3, sep = '\n')

# end 사용하기
    # print()는 기본적으로 출력하는 값 끝에 '\n'을 붙인다.
print(1, end = '')
print(2, end = '')
print(3, end = '\n')
print('x')

# 날짜/시간 출력 예
year = 2021
month = 11
day = 16
hour = 11
